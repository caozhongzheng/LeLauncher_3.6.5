package com.lenovo.lejingpin.hw.content.data;

import android.graphics.drawable.Drawable;

public class AppSnapshot {
	
	private String appimgMime;
	private String appimgName;
	private String appimgId;
	private String lcaid;
	private String appimgPath;
	private String appimgTime;
	private Drawable appimgIcon;
	public String getAppimgMime() {
		return appimgMime;
	}
	public void setAppimgMime(String appimgMime) {
		this.appimgMime = appimgMime;
	}
	public String getAppimgName() {
		return appimgName;
	}
	public void setAppimgName(String appimgName) {
		this.appimgName = appimgName;
	}
	public String getAppimgId() {
		return appimgId;
	}
	public void setAppimgId(String appimgId) {
		this.appimgId = appimgId;
	}
	public String getLcaid() {
		return lcaid;
	}
	public void setLcaid(String lcaid) {
		this.lcaid = lcaid;
	}
	public String getAppimgPath() {
		return appimgPath;
	}
	public void setAppimgPath(String appimgPath) {
		this.appimgPath = appimgPath;
	}
	public String getAppimgTime() {
		return appimgTime;
	}
	public void setAppimgTime(String appimgTime) {
		this.appimgTime = appimgTime;
	}
	public Drawable getAppimgIcon() {
		return appimgIcon;
	}
	public void setAppimgIcon(Drawable appimgIcon) {
		this.appimgIcon = appimgIcon;
	}
	
	
	
	

}
